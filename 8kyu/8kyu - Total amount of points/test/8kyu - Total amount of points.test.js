const points = require('../8kyu - Total amount of points');
const should = require('should');

describe('8kyu - Total amount of points', () => {
  it('Should return 30 for points(["1:0","2:0","3:0","4:0","2:1","3:1","4:1","3:2","4:2","4:3"])', (done) => {
    should(points(["1:0","2:0","3:0","4:0","2:1","3:1","4:1","3:2","4:2","4:3"])).equal(30);
    done();
  });
  it('Should return 10 for points(["1:1","2:2","3:3","4:4","2:2","3:3","4:4","3:3","4:4","4:4"])', (done) => {
    should(points(["1:1","2:2","3:3","4:4","2:2","3:3","4:4","3:3","4:4","4:4"])).equal(10);
    done();
  });  
  it('Should return 0 for points(["0:1","0:2","0:3","0:4","1:2","1:3","1:4","2:3","2:4","3:4"])', (done) => {
    should(points(["0:1","0:2","0:3","0:4","1:2","1:3","1:4","2:3","2:4","3:4"])).equal(0);
    done();
  });  
  it('Should return 15 for points(["1:0","2:0","3:0","4:0","2:1","1:3","1:4","2:3","2:4","3:4"])', (done) => {
    should(points(["1:0","2:0","3:0","4:0","2:1","1:3","1:4","2:3","2:4","3:4"])).equal(15);
    done();
  });  
  it('Should return 12 for points(["1:0","2:0","3:0","4:4","2:2","3:3","1:4","2:3","2:4","3:4"])', (done) => {
    should(points(["1:0","2:0","3:0","4:4","2:2","3:3","1:4","2:3","2:4","3:4"])).equal(12);
    done();
  });  
});